package calculs;

/**
 * <b>Mòdul que calcula el mòdul de dos nombres</b>
 * @version 1.0
 * @author Grup 1
 */
public class Modul {

    /**
     *
     * @param x primer valor
     * @param y segon valor
     * @return int resultat x%y
     */
    public static int modul(int x,int y){
        return x % y;
    }
}
